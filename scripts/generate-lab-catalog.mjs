#!/usr/bin/env node
/**
 * Generate shared/src/generated/labCatalog.ts from each module's lab files.
 *
 * Each app in apps/<slug>/src/data/labs/*.ts exports a single LabManifest
 * constant with `id` and `title` as string literals. We extract those via
 * regex — safer than trying to cross-boundary-import six separate apps
 * with their own tsconfigs — and emit a single typed array for the portal
 * (and anything else in @dci/shared) to consume.
 *
 * Run manually: pnpm -w generate:catalog
 * CI/build wiring: hooked into `shared` via pnpm `prebuild`/`pretypecheck`.
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");

// Keep this mirrored with shared/src/modules.ts MODULES order.
const MODULE_SLUGS = [
  "cybersecurity",
  "cloud-computing",
  "data-analytics",
  "programming",
  "networking",
  "it-support",
];

// Keys appear as either `id:` (bareword) or `"id":` (quoted) — same for title.
const ID_RE = /(?:^|[\s,{])"?id"?\s*:\s*"([^"]+)"/m;
const TITLE_RE = /(?:^|[\s,{])"?title"?\s*:\s*"((?:[^"\\]|\\.)*)"/m;

function extract(file) {
  const src = readFileSync(file, "utf8");
  const idMatch = src.match(ID_RE);
  const titleMatch = src.match(TITLE_RE);
  if (!idMatch || !titleMatch) {
    throw new Error(`Could not extract id/title from ${file}`);
  }
  // Titles may contain escaped quotes; unescape for the generated output.
  const title = titleMatch[1].replace(/\\"/g, '"').replace(/\\\\/g, "\\");
  return { id: idMatch[1], title };
}

/**
 * Derive the authoritative lab file list from the app's catalog.ts by
 * reading its `from "./labs/<name>"` imports. This avoids mistaking helper
 * modules (e.g. programming's labs/shared.ts factory) for lab manifests.
 */
function listLabFilesFromCatalog(slug) {
  const catalogPath = join(
    repoRoot,
    "apps",
    slug,
    "src",
    "data",
    "catalog.ts",
  );
  const src = readFileSync(catalogPath, "utf8");
  const re = /from\s+"\.\/labs\/([^"]+)"/g;
  const names = new Set();
  let m;
  while ((m = re.exec(src)) !== null) names.add(m[1]);
  return [...names]
    .sort()
    .map((name) => join(repoRoot, "apps", slug, "src", "data", "labs", `${name}.ts`));
}

function collect() {
  /** @type {{ moduleSlug: string; labId: string; title: string }[]} */
  const entries = [];
  for (const slug of MODULE_SLUGS) {
    for (const file of listLabFilesFromCatalog(slug)) {
      const { id, title } = extract(file);
      entries.push({ moduleSlug: slug, labId: id, title });
    }
  }
  return entries;
}

function escapeString(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function render(entries) {
  const lines = [];
  lines.push("// AUTO-GENERATED by scripts/generate-lab-catalog.mjs");
  lines.push("// Do not edit by hand — run `pnpm -w generate:catalog`.");
  lines.push("");
  lines.push("export interface LabCatalogEntry {");
  lines.push("  /** Module slug (matches DciModule.slug and Firestore `module`). */");
  lines.push("  moduleSlug: string;");
  lines.push("  /** Lab id (matches Firestore `labId`). */");
  lines.push("  labId: string;");
  lines.push("  /** Human-readable lab title from the lab manifest. */");
  lines.push("  title: string;");
  lines.push("}");
  lines.push("");
  lines.push("export const LAB_CATALOG: readonly LabCatalogEntry[] = [");
  for (const e of entries) {
    lines.push(
      `  { moduleSlug: "${escapeString(e.moduleSlug)}", labId: "${escapeString(e.labId)}", title: "${escapeString(e.title)}" },`,
    );
  }
  lines.push("] as const;");
  lines.push("");
  return lines.join("\n");
}

function main() {
  const entries = collect();
  const outDir = join(repoRoot, "shared", "src", "generated");
  mkdirSync(outDir, { recursive: true });
  const outFile = join(outDir, "labCatalog.ts");
  writeFileSync(outFile, render(entries), "utf8");
  // One-line summary is enough; per-module breakdown shows up if something's off.
  const perModule = {};
  for (const e of entries) {
    perModule[e.moduleSlug] = (perModule[e.moduleSlug] ?? 0) + 1;
  }
  const summary = MODULE_SLUGS.map((s) => `${s}=${perModule[s] ?? 0}`).join(", ");
  console.log(`[generate-lab-catalog] wrote ${entries.length} entries (${summary})`);
}

main();
